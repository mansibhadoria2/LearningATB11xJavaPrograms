package ex_09_DoWhile;

public class Lab02_Differnce_While_DoWhile_ {
    static void main(String[] args) {
        int i = 10;
// Do While Example
        do {
            System.out.println("i: " + i);
            i++;
        } while (i < 1);

        //while Example
        int j = 5;
        while (j < 1) {
            System.out.println("j: " + j);
        }
    }
}
