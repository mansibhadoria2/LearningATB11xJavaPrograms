package ex_07_For_Loop;

public class Lab03_For_If_Loop {
    public static void main(String[] args) {
        for (int mansi=0;mansi<18;mansi++){
           if(mansi>15){
               System.out.println("Allowed to go Movie");
           } else {
               System.out.println("Not Allowed to go Movie");
           }
        }
    }
}
