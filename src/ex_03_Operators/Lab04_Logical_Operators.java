package ex_03_Operators;

public class Lab04_Logical_Operators {
    public static void main(String[] args) {
        boolean a= true;
        System.out.println("a=" +a);
        System.out.println("!a=" +!a);

        boolean b=true;
        System.out.println("!!b ="+!!b);

        boolean c= true|| false; // OR Gate
        System.out.println("c=" + c);

    }
}
