package ex_03_Operators;

public class Lab08_Compound_Operators {
    public static void main(String[] args) {
        //Compound Assignment Operators
        // += , -= , /= , *=
        int age=10;
        age += 5; //age=age+5;
        System.out.println(age);

        age -=3; //age=age-3;
        System.out.println(age);

        age /= 2; //age =age/2;
        System.out.println(age);

    }
}
