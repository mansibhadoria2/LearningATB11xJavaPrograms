package ex_03_Operators;

public class Lab06_Equals_Operator {
    public static void main(String[] args) {
        // == compares the value
        System.out.println(10 == 10);
        System.out.println(10 < 10);
        System.out.println(10 == 20);
        System.out.println(10 >= 10);
        System.out.println(!(10 > 10));
    }
}
