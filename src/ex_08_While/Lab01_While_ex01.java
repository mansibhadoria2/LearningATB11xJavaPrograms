package ex_08_While;

public class Lab01_While_ex01 {
    public static void main(String[] args) {
        int i=0; //intialisation
        System.out.println("Start the Program using the while loop");
        while (i<5){  //condition (must be true to enter the loop)
            System.out.println("i="+i);
            i++;  //updation
        }
    }
}
